package Java1Project;

public class ForExam {

	public static void main(String[] args) {
		
		for(int i=0; i<10; i++) {
			System.out.println("i = " + i);
		}
		// for 반복문
		// i=0; -> 초기화(초기값 설정) : 처음에만 사용
		// i<10 -> 몇번 반복할 것인가?
		// i++ -> 증감식 
		
		for(;;) { //무한 루프
			System.out.println("안녕");
		}

		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
	}
}
